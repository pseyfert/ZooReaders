const ZooP* UpdatedMass::assocLong(const ZooP* JPsi) {
  const ZooP* part = vmleg(JPsi);
  if (0==part->Links()->NLinks()) return NULL;
    float max = 0.;
    int maxind = -1;
    for (unsigned int i = 0 ; i < part->Links()->NLinks();++i) {
      if (part->Links()->Frac(i) > 0.5 && max < part->Links()->Frac(i)) {
        max = part->Links()->Frac(i);
        maxind = i;
      }
    }
    if (max<0.95) return NULL;
    return part->Links()->Link(maxind);
}

float UpdatedMass::eval(const ZooP* p) {
  const ZooP* vmleglong = assocLong(p);
  const ZooP* lleg = longleg(p);
  if (NULL == vmleglong) return std::numeric_limits<float>::quiet_NaN();
  zooLorentzVector mothermom = vmleglong->momentum();
  mothermom+=lleg->momentum();
  return p->measuredMass() - mothermom.M();
}
  

const ZooP* UpdatedMass::vmleg(const ZooP* JPsi) {
  const ZooP* vmleg = NULL;
  const ZooP* ch1 = (*JPsi)[0];
  const ZooP* ch2 = (*JPsi)[1];
  if (ch1->TrInfo() && 0==ch1->TrInfo()->history())
    vmleg = ch1;
  if (ch2->TrInfo() && 0==ch2->TrInfo()->history()) {
    if (vmleg) std::cout << "watch out!" << std::endl;
    vmleg = ch2;
  }
  if (NULL == vmleg) std::cout << "VM leg not found" << std::endl;
  return vmleg;
}

const ZooP* UpdatedMass::longleg(const ZooP* JPsi) {
  const ZooP* lleg = NULL;
  const ZooP* ch1 = (*JPsi)[0];
  const ZooP* ch2 = (*JPsi)[1];
  if (ch1->TrInfo() && 0!=ch1->TrInfo()->history())
    lleg = ch1;
  if (ch2->TrInfo() && 0!=ch2->TrInfo()->history()) {
    if (lleg) std::cout << "watch out!" << std::endl;
    lleg = ch2;
  }
  if (NULL == lleg) std::cout << "long leg not found" << std::endl;
  return lleg;
}

float ZooMinChild::eval(const ZooP* part) {
    float mini = method->eval((*part)[0]);
    for (unsigned k = 1 ; k < part->NCh() ; ++k) {
      mini = std::min(mini,method->eval((*part)[k]));
    }
    return mini;
}

