float ZooJPsi::eval(const ZooP* part) {
   /// 443 = Jpsi
   if (443 == part->pid()) return method->eval(part);
   std::vector<const ZooP*> parts;
   parts.clear();
   parts.push_back(part);
   int infiniteloop = 0;
   while (parts.size()) {
     if (200<++infiniteloop) {std::cerr << "infinite recursion???" <<std::endl; return std::numeric_limits<float>::quiet_NaN();}
     if (443 == parts.back()->pid()) return method->eval(parts.back());
     const ZooP* p = parts.back();
     parts.pop_back();
     for (unsigned i = 0 ; i < p->NCh() ; ++i)
       parts.push_back(p->Ch(i));
   }
   std::cerr << "no JPsi found in decay tree" << std::endl;
   return std::numeric_limits<float>::quiet_NaN();
}

