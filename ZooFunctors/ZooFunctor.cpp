float ZooPtChild::eval(const ZooP* part) {
  std::vector<float> sortedpts;
  std::vector<unsigned> sortedlegs;
  sortedpts.clear();
  sortedlegs.clear();
  for (unsigned k = 0 ; k<part->NCh() ; ++k) {
    for (unsigned i = 0 ; i < sortedpts.size() ; ++i) {
      if (part->Ch(k)->momentum().Rho() > sortedpts[i]) {
        sortedlegs.insert(sortedlegs.begin()+i,k);
        sortedpts.insert(sortedpts.begin()+i,part->Ch(k)->momentum().Rho());
        break;
      }
    }
    if (k+1>sortedpts.size()) {
      sortedlegs.push_back(k);
      sortedpts.push_back(part->Ch(k)->momentum().Rho());
    }
  }
   
  return method->eval((*part)[sortedlegs[m_leg]]);
}



